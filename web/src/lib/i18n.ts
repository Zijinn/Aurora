export type Translator = (key: string) => string

export function translate(locale: Locale, key: string): string {
  return messages[locale][key] ?? messages["en-US"][key] ?? key
}

export function useTranslation() {
  const locale = useReaderStore((state) => state.locale)
  return { locale, t: ((key: string) => translate(locale, key)) as Translator }
}

export function localizedScopeTitle(scope: { kind: string; title: string }, locale: Locale) {
  const keyByKind: Record<string, string> = { today: "today", unread: "unread", saved: "saved", all: "allFeeds" }
  return keyByKind[scope.kind] ? translate(locale, keyByKind[scope.kind]!) : scope.title
}
